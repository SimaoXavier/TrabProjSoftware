    private void btnSalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSalvarActionPerformed
        // Adicionar Cliente
        Crianca obj = new Crianca();
        
        obj.setNome(txtNome.getText());
        obj.setData_nasc(txtDataNasc.getText());
        obj.setSexo(txtSexo.getSelectedItem().toString());
        obj.setData_cadastro(txtDataCadastro.getText());
        obj.setStatus(txtStatus.getText());
        
        CriancaDAO dao = new CriancaDAO();
        dao.cadastrarCrianca(obj);
    }//GEN-LAST:event_btnSalvarActionPerformed

