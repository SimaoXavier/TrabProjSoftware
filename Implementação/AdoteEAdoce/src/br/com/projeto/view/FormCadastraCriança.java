    public void listar(){
        CriancaDAO dao = new CriancaDAO();
        List<Crianca> lista = dao.listarCriancas();
            DefaultTableModel dados = (DefaultTableModel) tabelaCrianca.getModel();
            dados.setNumRows(0);
            for(Crianca c : lista){
                dados.addRow(new Object[]{
                    c.getCodigo(),
                    c.getData_nasc(),
                    c.getSexo(),
                    c.getStatus(),
                    c.getData_cadastro()
                
                });
            }
    }
